				d_In.add(new ArcIn(generated_task, generate_task_n_pages, 1));
				final var task_n_pages = new PetriP(task_n_pages_name, 0);
				d_P.add(task_n_pages);
				d_Out.add(new ArcOut(generate_task_n_pages, task_n_pages, 1));

